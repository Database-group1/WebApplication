<h2>野菜の育て方アドバイス</h2>
<h3>カボチャ</h3>
<div class="box29">
<div class="box-title">種まき、苗作り</div>
<p>直径4～5cm、深さ1cmくらいの穴を作り、4～5粒をまいて土をかぶせ、手で軽く押さえて水やりします。25～30℃前後になるよう保温します。本葉1～2枚の時に2本に間引きし、本葉2～3枚で1本に間引きする。</p>
</div>
<div class="box29">
<div class="box-title">畝作り</div>
<ul>
<li>肥料</li>
2週間前に苦土石灰をまいて深く耕す。1週間前に直径30～40cm、深さ30cm程の穴を掘り、底に堆肥1㎏を入れ、掘り上げた土に化成肥料50g、過リン酸石灰20gをかきまぜて穴に埋め戻す。
<li>畝の形</li>
埋め戻した周りの土を集めて60㎝四方の高さ10cm程の定植床または直まき床を作ります。株間は1mほどにします。
</ul>
</div>
<div class="box29">
<div class="box-title">栽培管理</div>
<ul>
<li>生育管理</li>
親づる1本と子づる1本にするため、着果前は他の子･孫づるはかき取り、後は込みすぎたつるを摘除する。
カボチャは雄花雌花があるので、着果を確実にするため雄花の花粉を雌しべにつける人工授粉でもよい。
<li>害虫</li>
<ul>
<li>アブラムシ類</li>
有効薬剤:スタークル顆粒水溶剤、アルバリン顆粒水溶剤、ダントツ水溶剤、コルト粒剤水和剤
<li>ウリキンウワバ</li>
有効薬剤:
<li>ウリハムシ</li>
有効薬剤:ダントツ水溶剤、トレボン乳剤
<li>ハダニ類</li>
有効薬剤:カネマイトフロアブル、モベントフロアブル、ニッソラン水和剤
</ul>
<li>病気</li>
<ul>
<li>モザイク病</li>
感染元であるアブラムシの飛来防止及び殺虫。
<li>褐斑細菌病</li>
有効薬剤:コサイド3000、Ｚボルドー
<li>斑点細菌病</li>
有効薬剤:コサイド3000、Ｚボルドー
<li>どんこ病</li>
有効薬剤:パンチョＴＦ顆粒水和剤、ベルクート水和剤、モレスタン水和剤、フルピカフロアブル、カルビオ、ガッテン乳剤、ラミック顆粒水和剤
<li>つる枯病</li>
有効薬剤:ジマンダイセン水和剤、ダコニールエース
<li>疫病</li>
有効薬剤:、ランマンフロアブル、ライメイフロアブル、ジマンダイセン水和剤、リドミルゴールドＭＺ、アリエッティ水和剤
</ul>
</ul>
</div>
<div class="box29">
<div class="box-title">収穫</div>
<p>西洋種は開花後40～45日ほど経って、果梗のひび割れが十分コルク化したら収穫します。</p>
