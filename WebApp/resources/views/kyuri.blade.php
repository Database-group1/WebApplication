有効薬剤:ロブラール水和剤、スミレックス水和剤、ジマンダイセン水和剤、トップジンＭ水和剤、ＧＦベンレート水和剤
<li>うどんこ病</li>
有効薬剤:ベジセイバー、アフェットフロアブル、ケンジャフロアブル、ラミック顆粒水和剤、パンチョ顆粒水和剤、プロパティフロアブル、ベルクートフロアブル、ガッテン乳剤、パルミノ
<li>灰色かび病</li>
有効薬剤:ファンタジスタ顆粒水和剤、ピクシオDF、ネクスターフロアブル、アフェットフロアブル、カンタスドライフロアブル、セイビアーフロアブル20、ラミック顆粒水和剤、ベルクートフロアブル
</ul>
</ul>
</div>
<div class="box29">
<div class="box-title">収穫</div>
<p>つるも果実もあっという間に大きくなるので、栽培している品種の特性に応じて適期を逃さずに収穫してください。普通品種の果実は、夏季では開花から約1週間で収穫適期になります。収穫後は畑の肥料分も消費しているので、2週間に1回程度追肥を行います。</p>
