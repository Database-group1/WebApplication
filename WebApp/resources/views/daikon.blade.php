<h2>野菜の育て方アドバイス</h2>
<h3>大根</h3>
<div class="box29">
<div class="box-title">種まき、苗作り</div>
<p>畝にあけた穴に6粒まき、1cm程土で覆い軽く押さえます。発芽後、1回目の間引きは子葉が完全に開いた時に形のよいものを残して3本立ちにし、2回目は本葉3枚の時に生育が中くらいのものを残して2本立ちにします。3回目は本葉7枚の時、元気のよいものを残して1本立ちにします。</p>
</div>
<div class="box29">
<div class="box-title">畝作り</div>
<ul>
<li>肥料</li>
2週間以上前に苦土石灰を1㎡あたり150gとよく完熟堆肥2㎏を全面に散布して35cmくらい深く耕します。1週間前に化成肥料150gをまいて再度深く耕します。
<li>畝の形</li>
幅70㎝の畝を作り中央に30㎝間隔で直径10㎝、深さ1.5cm程の植え穴をあけます。
</ul>
</div>
<div class="box29">
<div class="box-title">栽培管理</div>
<ul>
<li>生育管理</li>
間引き後は株がふらつきやすくなっているので土寄を丁寧にします。その時肥料が株にかからないように注意します。
<li>害虫</li>
<ul>
<li>ヨトウムシ類</li>
有効薬剤:プレバソンフロアブル５、アクセルフロアブル、プレオフロアブル、ディアナSC
<li>アオムシ</li>
有効薬剤:エスマルクＤＦ、デルフィン顆粒水和剤、アファーム乳剤、トレボン乳剤、プレオフロアブル、ディアナＳＣ
<li>アブラムシ類</li>
有効薬剤:ベストガード水溶剤、ダントツ水溶剤、アクタラ顆粒水溶剤、ウララＤＦ
<li>キスジノミハムシ</li>
有効薬剤:スタークル顆粒水溶剤、アルバリン顆粒水溶剤、アクセルフロアブル
<li>ハイマダラノメイガ</li>
有効薬剤:プレバソンフロアブル５、フェニックス顆粒水和剤、アクセルフロアブル、アニキ乳剤、ディアナＳＣ
<li>コナガ</li>
有効薬剤:プレオフロアブル、トルネードエースDF、ディアナSC、アファーム乳剤
</ul>
<li>病気</li>
<ul>
<li>モザイク病</li>
アブラムシの飛来防止及び殺虫
<li>黒腐病</li>
有効薬剤:Zボルドー、コサイド3000
<li>黒斑細菌病</li>
有効薬剤:カスミンボルドー、Ｚボルドー、カセット水和剤、ヨネポン水和剤、マスターピース
<li>軟腐病</li>
有効薬剤:カスミンボルドー、カセット水和剤、キンセット水和剤80、ヨネポン水和剤、スターナ水和剤、マスタピース水和剤
<li>炭疽病</li>
有効薬剤:ダコニール1000
<li>黒斑病</li>
有効薬剤:4-4式ボルドー
</ul>
</ul>
</div>
<div class="box29">
<div class="box-title">収穫</div>
<p>秋ダイコンはタネまき後60～90日、夏ダイコンは50～60日で収穫期になります。目で見た場合の収穫期の判断は、外側の葉が垂れ、中心部の葉が横に開いて平らに見えるようになった時です。</p>
