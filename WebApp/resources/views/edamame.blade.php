<h2>野菜の育て方アドバイス</h2>
<h3>枝豆</h3>
<div class="box29">
<div class="box-title">種まき、苗作り</div>
<p>タネまきは、直まきとポットまきいずれも、直径5cm、深さ2cmくらいの穴を作り、4粒まき、元の面と同じになるよう土で覆います。子葉が開き始めた時に間引いて、2本立ちにします。ポットまきでは初生葉が展開したらタネまき後15日で定植します。</p>
</div>
<div class="box29">
<div class="box-title">畝作り</div>
<ul>
<li>肥料</li>
定植2週間前までに苦土石灰を1㎡あたり150g全面散布し、深く耕します。1週間前に堆肥1㎏と化成肥料100g、過リン酸石灰50gをまいて耕します。
<li>畝の形</li>
1列の場合は幅40㎝で高さ10㎝の畝を作り中央に20㎝間隔で植え穴をあけます。2列の場合は幅60㎝で高さ10㎝の畝を作り、列の間を30㎝開けて25㎝間隔で植え穴をあけます。
</ul>
</div>
<div class="box29">
<div class="box-title">栽培管理</div>
<ul>
<li>生育管理</li>
株のまわりを軽く耕し、株元に土を寄せる作業を3回行いますが、最後の土寄せは開花が始まる頃までに終わらせます。土が乾く時はマルチや敷きワラ、水やりをすると効果があります。
<li>害虫</li>
<ul>
<li>アブラムシ類</li>
有効薬剤:アクタラ顆粒水溶剤、ダントツ水溶剤、ウララＤＦ
<li>カメムシ類</li>
有効薬剤:アクタラ顆粒水溶剤、スタークル顆粒水溶剤、アルバリン顆粒水溶剤、トレボン乳剤、キラップフロアブル
<li>ダイズサヤタマバエ</li>
有効薬剤:トレボン乳剤、アルバリン顆粒水溶剤、スタークル顆粒水溶剤
<li>シロイチモジマダラメイガ</li>
有効薬剤:トレボン乳剤、スミチオン乳剤
<li>ハスモンヨトウ</li>
有効薬剤:トルネードエースDF、アファーム乳剤、フェニックスフロアブル、プレバソンフロアブル５、プレオフロアブル
<li>ウコンノメイガ</li>
有効薬剤:トレボン乳剤、プレバソンフロアブル５、フェニックスフロアブル
</ul>
<li>病気</li>
<ul>
<li>モザイク病</li>
アブラムシの飛来防止及び殺虫
<li>わい化病</li>
アブラムシの飛来防止及び殺虫
<li>べと病</li>
有効薬剤:、ランマンフロアブル、ライメイフロアブル、フェスティバルＣ、アミスター20フロアブル、レーバスフロアブル水和剤
<li>紫斑病</li>
有効薬剤:ファンタジスタ顆粒水和剤、ニマイバー水和剤、ゲッター水和剤、ベンレート水和剤
<li>菌核病</li>
有効薬剤:ロブラール水和剤、ベンレート水和剤、トップジンＭ水和剤、ファンタジスタ顆粒水和剤、ニマイバー水和剤
<li>さび病</li>
有効薬剤:カリグリーン、ハーモメイト水溶剤
</ul>
</ul>
</div>
<div class="box29">
<div class="box-title">収穫</div>
<p>タネまきから収穫までの期間は極早生種で80～90日、早生・中生種で90～110日ほどです。収穫の適期幅は5～7日です。株の中央部の莢がふくらんで、実がかたくならない程度の株を、株ごと引き抜くか、熟した莢から順に収穫します。</p>
