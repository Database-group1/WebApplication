<h2>野菜の育て方アドバイス</h2>
<h3>インゲン豆</h3>
<div class="box29">
<div class="box-title">種まき、苗作り</div>
<p>直まきは直径7cm、深さ3cm程度に作った穴に、ポットまきは土をポットの上端から3cmほど低いところまで入れたポットに、4粒をまき、それぞれ2cm程土で覆ってから水やりをします。発芽後、ポットまきは初生葉が展開し始めたら、直まきは本葉2枚になったら、2本立てとします。ポットまきでは本葉が2枚になったら定植します。タネまき後に土が湿りすぎていると腐りやすいので、水やりはあっさりとします。間引き後は、残した株の株元の土を軽く押して、株がぐらつかないようにします。</p>
</div>
<div class="box29">
<div class="box-title">畝作り</div>
<ul>
<li>肥料</li>
定植2週間前までに苦土石灰を1㎡あたり100g全面散布し、深く耕します。1週間前に堆肥3㎏と化成肥料150gをまいて耕します。
<li>畝の形</li>
つるあり種は幅120cm、高さ20㎝の畝を作り、中央に40㎝間隔で植え穴をあけます。つるなし種は幅75cm、高さ20㎝の畝を作り、間隔25㎝の2列にして30㎝間隔で植え穴をあけます。
</ul>
</div>
<div class="box29">
<div class="box-title">栽培管理</div>
<ul>
<li>生育管理</li>
つるあり種は本葉4枚になるとつるが伸び始めるので、つるが巻きつくための支柱やネットを立てます。つるなし種は倒伏を防ぐため、短い支柱を立てて枝を軽く縛ります。
<li>害虫</li>
<ul>
<li>アブラムシ類</li>
有効薬剤:アクタラ顆粒水溶剤、スタークル顆粒水溶剤、アルバリン顆粒水溶剤
<li>ハスモンヨトウ</li>
有効薬剤:プレバソンフロアブル５、フェニックス顆粒水和剤、プレオフロアブル
<li>ハダニ類</li>
有効薬剤:コロマイト乳剤、ダブルフェースフロアブル、ニッソラン水和剤
<li>ミナミキイロアザミウマ</li>
有効薬剤:ディアナSC
<li>アズキノメイガ</li>
有効薬剤:フェニックス顆粒水和剤
</ul>
<li>病気</li>
<ul>
<li>つる枯病</li>
アブラムシの飛来防止及び殺虫
<li>モザイク病</li>
アブラムシの飛来防止及び殺虫
<li>かさ枯病</li>
有効薬剤:ドイツボルドーＤＦ、カスミンボルドー、Ｚボルドー、クプラビットホルテ、ドイツボルドーＡ、コサイド3000
<li>炭疽病</li>
有効薬剤:、ゲッター水和剤、トップジンM水和剤、ケンジャフロアブル、ファンタジスタ顆粒水和剤、フロンサイドSC
<li>さび病</li>
有効薬剤:、カリグリーン、ハーモメイト水溶剤
<li>角斑病</li>
有効薬剤:トップジンM水和剤、ベンレート水和剤
</ul>
</ul>
</div>
<div class="box29">
<div class="box-title">収穫</div>
<p>つるなし種は、タネまき後40～50日くらいで収穫が始まり、収穫期間は15日くらいです。つるあり種はタネまき後65～70日くらいで収穫が始まり、1カ月は収穫できます。いずれも花が咲いてからは約2週間後で、実のふくらみがわずかに見えてきたら収穫適期です。</p>
</div>
