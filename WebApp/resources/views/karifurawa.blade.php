<h2>野菜の育て方アドバイス</h2>
<h3>カリフラワー</h3>
<div class="box29">
<div class="box-title">種まき、苗作り</div>
<p>ポットまきは直径3cm、深さ1cmの穴を作り、4粒をお互いに離してまきます。発芽後は本葉が出始めた頃に2本立ちに間引きし、本葉が2枚になる頃に1本立ちにします。箱まきの時は深さ1cmほどの溝を8cm間隔で作り、1粒ずつ1cm間隔でまきます。発芽後は本葉が出始めた頃に2cm間隔に間引きして、本葉が2枚程度になったらポットに移植します。5mm程土で覆い、たっぷりと水やりします。夏まきでは定植近くになったら寒冷紗などをはずし、苗を露地条件に慣れさせます。</p>
</div>
<div class="box29">
<div class="box-title">畝作り</div>
<ul>
<li>肥料</li>
定植2週間前に苦土石灰を1㎡あたり100g全面散布して深く耕し、1週間前に幅15㎝、深さ15㎝の溝を掘り、堆肥2㎏と化成肥料150gを入れて土を埋め戻します。
<li>畝の形</li>
土を埋め戻すときに幅40㎝の畝を作ります。苗が少し出るくらいの植え穴を畝の中央に40㎝間隔で開けます。
</ul>
</div>
<div class="box29">
<div class="box-title">栽培管理</div>
<ul>
<li>生育管理</li>
倒伏しやすいのでしっかりと土寄せしますし、花蕾が鶏卵ほどになったら、着色や寒害から保護するため、花蕾を包むように外葉を束ね、ひもで結びます。あるいは外葉を切って花蕾の上を覆う方法もあります。
<li>害虫</li>
<ul>
<li>コナガ</li>
有効薬剤:プレオフロアブル、ディアナSC、アファーム乳剤、アニキ乳剤
<li>アオムシ</li>
有効薬剤:エスマルクＤＦ、デルフィン顆粒水和剤、アファーム乳剤、プレバソンフロアブル５
<li>ヨトウムシ類</li>
有効薬剤:ディアナＳＣ、プレバソンフロアブル５、アニキ乳剤、プレオフロアブル
<li>タマナギンウワバ</li>
有効薬剤:ディアナＳＣ、プレバソンフロアブル５、アニキ乳剤、プレオフロアブル
<li>ハイマダラノメイガ</li>
有効薬剤:ディアナＳＣ、フェニックス顆粒水和剤、プリンスフロアブル
<li>アブラムシ類</li>
有効薬剤:ベストガード水溶剤、アクタラ顆粒水溶剤、ウララDF
</ul>
<li>病気</li>
<ul>
<li>軟腐病</li>
有効薬剤:スターナ水和剤、Zボルドー
<li>黒腐病</li>
有効薬剤:ヨネポン水和剤、コサイド3000、Zボルドー
<li>べと病</li>
有効薬剤:ライメイフロアブル、ランマンフロアブル、ダコニール1000
<li>黒斑細菌病</li>
有効薬剤:Zボルドー
